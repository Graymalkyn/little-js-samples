function mapToRoundScore(arr){
  var result = [];
  arr.forEach(function(user){
    result.push({
      login: user.login.toUpperCase(),
      id: user.id,
      score: Math.round(user.score)
    });
  });
  return result;
}

var roundScoreResult = mapToRoundScore(data.items);
